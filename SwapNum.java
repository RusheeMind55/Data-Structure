package InterviewPractice;

public class SwapNum {
	
	public static void main(String[] args) {
		
		int a=10;
	
		int b=20;
	
		System.out.println("Before swaaping "+a +" "+ b);
		
		int t=a; // t=10.
			a=b; // 10=20(a).
			b=t; //20=10 (b).
		
		System.out.println("After swaaping"+a+" "+b);  
		
			
	}
	

}
